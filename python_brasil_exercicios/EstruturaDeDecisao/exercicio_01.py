numero_um = int(input("Digite o primeiro numero: "))
numero_dois = int(input("Digite o segundo numero: "))

if numero_um > numero_dois:
    print("O numero {0} e maior que o numero {1}".format(numero_um, numero_dois))
else:
    print("O numero {0} e maior que o numero {1}".format(numero_dois, numero_um))
